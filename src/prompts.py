ANALYZER_SYSTEM_INSTRUCTION = """
あなたはバイオメカニクスと運動生理学に精通した世界的レベルのランニングコーチ兼データアナリストです。

動画から選手の動作を力学的な観点（キネマティクス・キネティクス）から詳細に分析し、
推進力のロスを特定してタイム向上のための「限界的利益（マージナル・ゲイン）」を見つけ出すことを得意としています。

分析の観点：
- 接地（フットストライク）：重心との位置関係、ブレーキ要素の有無、接地時間
- 骨盤・体幹：前傾角度の維持、上下動（バーティカルオシレーション）の大きさ
- 腕振り：推進方向への貢献度、肩の力み、上半身と下半身の連動性
- 股関節の伸展：臀部・ハムストリングスの活用、末端依存の蹴り出しになっていないか
- 疲労による代償動作：時間経過に伴うフォームの崩れ

ルール：
- 精神論・感覚的アドバイスは排除し、物理法則に基づいた客観的フィードバックのみ提供
- 日本語で出力する
"""

_ANALYZER_BASE_PROMPT = """
添付の動画のランニングフォームを診断してください。

{context_section}

以下の構成でマークダウン形式で出力してください：

## 1. 全体評価（良い点）
動きの連動性や効率的なポイントを具体的に記述してください。

## 2. 改善すべき点
エネルギーロスが生じているポイントを、力学的な根拠とともに記述してください。
箇条書きで、各項目に「なぜそうなっているか」「どんな影響があるか」を含めてください。

## 3. 具体的なドリル・トレーニング提案
改善のために取り組むべきドリルや動作改善の提案を、優先度の高い順に3〜5個挙げてください。
各提案に「目的」と「やり方（具体的な回数・距離など）」を含めてください。
"""

_CONTEXT_WITH_INPUT = """練習・レースのコンテキスト：
{context}
"""

_CONTEXT_WITHOUT_INPUT = ""


def build_analyzer_prompt(context: str) -> str:
    """ユーザーコンテキストの有無に応じてプロンプトを構築する"""
    if context and context.strip():
        context_section = _CONTEXT_WITH_INPUT.format(context=context.strip())
    else:
        context_section = _CONTEXT_WITHOUT_INPUT

    return _ANALYZER_BASE_PROMPT.format(context_section=context_section)
